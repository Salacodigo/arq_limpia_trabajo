class Calculadora:
    def __init__(self):
        pass

    def sumar(self, number1, number2):
        return number1 + number2
    
    def restar(self, number1, number2):
        return number1 - number2
    
    def multiplicar(self, number1, number2):
        return number1 * number2
    
    def dividir(self, number1, number2):
        return number1 / number2